<h2>Famous Canadian Freemasons</h2>

<legend>Abbott, Sir John Joseph Caldwell</legend> 
<p>Canadian Prime Minister (1891 - 1892). Initiated St. Paul's, No. 374, E.R., Montreal, 1847.</p>

<legend>Badgley, William</legend>     
<p>Canadian Lawyer, first Dean of the Faculty of Law at McGill University, Montreal. 
St. Paul's Lodge, No. 374 UGLE, Montreal. Past Provincial Grand Master.</p>

<legend>Ballard, Harold</legend> 
<p>Owner of Toronto Maple Leafs National Hockey League team. 
Corinthian No. 481, GRC, Toronto, ON.</p>

<legend>Bennett, Viscount Richard Bedford</legend>   
<p>Canadian Prime Minister (1930 - 1935)</p>
 
<legend>Borden, Sir Robert Laird</legend>     
<p>Canadian Prime Minister (1911 - 1920). St. Andrew's Lodge No. 1, Halifax, Nova Scotia.</p>

<legend>Bowell, Sir Mackenzie</legend>   
<p>Canadian Prime Minister (1894 - 1896) (British Born).  
St. Lawrence Lodge No. 640 E.R. Montreal, PQ. Affiliated with Belleville Lodge No. 123, Belleville, ON.</p>

<legend>Brant, Joseph "Thayendanegea"</legend> 
<p>Principal Chief of the Six Nations Indians. Initiated in Lodge No. 417, 1776.
First Master of Lodge No. 11, Mohawk Village (near Brantford) in 1798.</p>

<legend>Conacher, Charlie</legend>    
<p>Toronto Maple Leaf ice hockey legend. 
Initiated in North Gate Lodge No. 591, Pickering, Ontario, in 1935.</p>

<legend>Currie, Sir Authur W.</legend>   
<p>Commanded the Canadian Corps in 1917 Zetland, No. 326, Toronto, Ontario.</p>

<legend>Diefenbaker, John George</legend>    
<p>Canadian Prime Minister (1957 - 1963) Initiated: September 11, 1922, Wakaw Lodge No. 166, Wakaw, Saskatchewan.</p>

<legend>Dunckerly, Thomas</legend>   
<p>Illegitimate born son of George II, consolidated the Royal Arch and was the first Grand Master of the Masonic Knights Templar. 
Acted as Grand Master for the warranted lodges in Quebec.</p>

<legend>Dunn, Alexander Robert</legend>   
<p>Canadian soldier who won Canada's first Victoria Cross at the Charge of the Light Brigade at Balaclava. Ionic Lodge No. 18 (now No. 25), Toronto, Ontario.</p>

<legend>Eaton, John David</legend>   
<p>President of the T. Eaton Company. Assiniboine, No. 114, G.R.M., Winnipeg.</p>

<legend>Eddy, E.D.</legend> 
 <p>Founder of the E.B. Eddy Match Company</p>
 
<legend>Fleming, Sir Sanford</legend>    <p>Creator of first Canadian stamp.  Canadian engineer and surveyor. Suggested the use of time zones. 
St. Andrew's No. 16, Toronto, Ontario.</p>

<legend>Horton, Tim</legend>  
<p>Canadian ice hockey legend. Founder of the donut chain bearing his name. 
Initiated in Kroy Lodge No. 676, Toronto, Ontario, in 1962.</p>

<legend>Jarvis, William</legend>  
<p>First Provincial Grand Master (Ancients) for Upper Canada.
Provincial Secretary for Upper Canada. </p>

<legend>Layton, Athol</legend>    
<p>Canadian professional wrestler. 
Initiated in Birch Cliff Lodge No. 612, Scarborough (Toronto), Ontario, in 1959. Potentate Rameses Temple A.A.O.N.M.S. 1968.</p>

<legend>MacDonald, Sir John Alexander</legend>    
<p>The first Prime Minister of the Dominion of Canada (1867 - 1873 and 1878-1891 when he died in office). Began the creation of rail service across Canada.   
St. John's Lodge No. 758, Kingston, Ontario. Honourary Past Grand Senior Warden.</p>

<legend>Maclean, John Bayne</legend>  
<p>Founder of Maclean's Magazine and President of Maclean's Publishing Co. 
Ionic Lodge No. 25, Toronto, ON.</p>

<legend>Massey, Hart</legend>    
<p>Massey-Ferguson farm equipment.</p>
 
<legend>McLaughlin, Colonel R. S.</legend>    
<p>Founder and President of the McLaughlin Carriage Co. which later became General Motors of Canada. 
Cedar Lodge No. 270, Oshawa, Ontario. Grand Steward in 1945, 75 year member in the Craft.  Royal Arch, Knight Templar, President of Oshawa Shrine Club.</p>

<legend>Molson, John</legend>     
<p>Founder of Molson Breweries. St. Paul's Lodge, No. 374 UGLE, Montreal. Past Provincial Grand Master.</p>

<legend>Naismith, James</legend>  
<p>Canadian-born American sports educator who invented the game of basketball.</p>
 
<legend>Owen, Most Rev. Derwyn T.</legend>    
<p>Archbishop of Toronto and Primate of all Canada. Ionic Lodge No. 25, Toronto, Ontario.</p>

<legend>Roman, Charles Lightfoot</legend>     
<p>Canadian physician, one of the first in the field of industrial medicine, served as Grand Master of the Grand Lodge in Canada and apparently was the first African American to head a 'mainstream' Grand Lodge in North America (1952).</p>
 
<legend>Service, Robert W.</legend>  
 <p>Canadian poet among whose works are "The Cremation of Sam McGee"</p>
 
<legend>Simcoe, Lord John Graves</legend>     
<p>First Lieutenant Governor of Upper Canada - now Ontario. 
Union Lodge No. 307, E.R. (Moderns), Exeter, England in 1773.</p>

<legend>Sinclair, Gordon</legend>     
<p>Canadian journalist and broadcaster.</p> 

<legend>John Ross</legend> <p>Robertson No. 545 Toronto, Ontario.</p>

<legend>Smallwood, Joseph</legend>   
<p>NFLD Premier</p>

<legend>Steele, Sir Samuel Benfield Major-General</legend> 
<p>commanding Canadian forces in England during WWI.</p>
 
<legend>Tecumseh, Chief</legend>     
<p>Canadian Native Indian Chief</p>
 
<legend>Watson, "Whipper" Billy</legend> 
<p>Canadian wrestling legend (1916 - 1990) Initiated in Temple, No 525, in 1944.</p>

<legend>Watson, Major-General Sir David</legend> 
<p>Commander of the Fourth Canadian Division 1916 - 1918</p>
 
<legend>Wilson, William Mercer</legend>   
<p>First Grand Master of the independent Grand Lodge of Canada, 1855. 
First Grand First Principal of Royal Arch Masons of Canada.</p>

<legend>Winch, Ernie</legend>    
<p>Canadian Labour Activist & Politician</p>
